package strings

import (
	"math/rand"
	"strings"
	"testing"
	"time"
	"unsafe"
)

var (
	testDataLen   = 2 << 5
	testDataCount = 2 << 8
	testData      = make([]string, 0, testDataCount)
)

func init() {
	for i := 0; i < testDataCount; i++ {
		testData = append(testData, randStr(testDataLen))
	}
}

var randSrc = rand.NewSource(time.Now().UnixNano())

const (
	rs6Letters       = "abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ"
	rs6LetterIdxBits = 6
	rs6LetterIdxMask = 1<<rs6LetterIdxBits - 1
	rs6LetterIdxMax  = 63 / rs6LetterIdxBits
)

func randStr(n int) string {
	b := make([]byte, n)
	cache, remain := randSrc.Int63(), rs6LetterIdxMax
	for i := n - 1; i >= 0; {
		if remain == 0 {
			cache, remain = randSrc.Int63(), rs6LetterIdxMax
		}
		idx := int(cache & rs6LetterIdxMask)
		if idx < len(rs6Letters) {
			b[i] = rs6Letters[idx]
			i--
		}
		cache >>= rs6LetterIdxBits
		remain--
	}
	// skipcq: GSC-G103
	return *(*string)(unsafe.Pointer(&b))
}

func BenchmarkStandardJoin(b *testing.B) {
	b.ReportAllocs()
	b.ResetTimer()
	b.RunParallel(func(pb *testing.PB) {
		for pb.Next() {
			s := strings.Join(testData, ":")
			_ = s
		}
	})
}

func BenchmarkValdJoin(b *testing.B) {
	b.ReportAllocs()
	b.ResetTimer()
	b.RunParallel(func(pb *testing.PB) {
		for pb.Next() {
			s := Join(testData, ":")
			_ = s
		}
	})
}
