func TestNewFilter(t *testing.T) {
	type args struct {
		opts []FilterOption
	}
	type want struct {
		wantEf Filter
		err    error
	}
	type test struct {
		name       string
		args       args
		want       want
		checkFunc  func(want, Filter, error) error
		beforeFunc func(args)
		afterFunc  func(args)
	}
	defaultCheckFunc := func(w want, gotEf Filter, err error) error {
		if !errors.Is(err, w.err) {
			return errors.Errorf("got error = %v, want %v", err, w.err)
		}
		if !reflect.DeepEqual(gotEf, w.wantEf) {
			return errors.Errorf("got = %v, want %v", gotEf, w.wantEf)
		}
		return nil
	}
	tests := []test{
		// TODO test cases
		/*
		   {
		       name: "test_case_1",
		       args: args {
		           opts: nil,
		       },
		       want: want{},
		       checkFunc: defaultCheckFunc,
		   },
		*/

		// TODO test cases
		/*
		   func() test {
		       return test {
		           name: "test_case_2",
		           args: args {
		           opts: nil,
		           },
		           want: want{},
		           checkFunc: defaultCheckFunc,
		       }
		   }(),
		*/
	}

	for _, test := range tests {
		t.Run(test.name, func(tt *testing.T) {
			defer goleak.VerifyNone(t)
			if test.beforeFunc != nil {
				test.beforeFunc(test.args)
			}
			if test.afterFunc != nil {
				defer test.afterFunc(test.args)
			}
			if test.checkFunc == nil {
				test.checkFunc = defaultCheckFunc
			}

			gotEf, err := NewFilter(test.args.opts...)
			if err := test.checkFunc(test.want, gotEf, err); err != nil {
				tt.Errorf("error = %v", err)
			}

		})
	}
}

